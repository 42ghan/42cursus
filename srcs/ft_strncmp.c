int		ft_strncmp(const char *s1, const char *s2, unsigned int n)
{
	int i;

	if (n == 0)
		return (0);
	i = 0;
	while (*(s1 + i) && *(s1 + i) == *(s2 + i) && n > 1)
	{
		i++;
		n--;
	}
	return (*(s1 + i) - *(s2 + i));
}
